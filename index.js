function change() {
  var colourCb = document.querySelectorAll(".colours input[type='checkbox']");
  var genderCb = document.querySelectorAll(".genders input[type='checkbox']");
  var priceCb = document.querySelectorAll(".price input[type='checkbox']");
  var typeCb = document.querySelectorAll(".types input[type='checkbox']");
  var filters = {
    colours: getClassOfCheckedCheckboxes(colourCb),
    genders: getClassOfCheckedCheckboxes(genderCb),
    prices: getClassOfCheckedCheckboxes(priceCb),
    types: getClassOfCheckedCheckboxes(typeCb)
  };

  filterResults(filters);
}

function getClassOfCheckedCheckboxes(checkboxes) {
  var classes = [];

  if (checkboxes && checkboxes.length > 0) {
    for (var i = 0; i < checkboxes.length; i++) {
      var cb = checkboxes[i];

      if (cb.checked) {
        classes.push(cb.getAttribute("rel"));
      }
    }
  }

  return classes;
}

function filterResults(filters) {
  var rElems = document.querySelectorAll(".result div");
  var hiddenElems = [];

  if (!rElems || rElems.length <= 0) {
    return;
  }

  for (var i = 0; i < rElems.length; i++) {
    var el = rElems[i];

    if (filters.colours.length > 0) {
      var isHidden = true;

      for (var j = 0; j < filters.colours.length; j++) {
        var filter = filters.colours[j];

        if (el.classList.contains(filter)) {
          isHidden = false;
          break;
        }
      }

      if (isHidden) {
        hiddenElems.push(el);
      }
    }

    if (filters.genders.length > 0) {
      var isHidden = true;

      for (var j = 0; j < filters.genders.length; j++) {
        var filter = filters.genders[j];

        if (el.classList.contains(filter)) {
          isHidden = false;
          break;
        }
      }

      if (isHidden) {
        hiddenElems.push(el);
      }
    }

    if (filters.prices.length > 0) {
      var isHidden = true;

      for (var j = 0; j < filters.prices.length; j++) {
        var filter = filters.prices[j];

        if (el.classList.contains(filter)) {
          isHidden = false;
          break;
        }
      }

      if (isHidden) {
        hiddenElems.push(el);
      }
    }

    if (filters.types.length > 0) {
      var isHidden = true;

      for (var j = 0; j < filters.types.length; j++) {
        var filter = filters.types[j];

        if (el.classList.contains(filter)) {
          isHidden = false;
          break;
        }
      }

      if (isHidden) {
        hiddenElems.push(el);
      }
    }
  }///////////////-------------i for loop ends here--------/////////////////

  for (var i = 0; i < rElems.length; i++) {
    rElems[i].style.display = "block";
  }

  if (hiddenElems.length <= 0) {
    return;
  }

  for (var i = 0; i < hiddenElems.length; i++) {
    hiddenElems[i].style.display = "none";
  }
}